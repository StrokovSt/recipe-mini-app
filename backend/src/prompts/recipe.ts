export const recipePrompt = (text: string) => `
Ты парсер рецептов. Извлеки данные из текста и верни ТОЛЬКО валидный JSON без markdown и пояснений.

ПРАВИЛА ИЗВЛЕЧЕНИЯ:
- title: название блюда. Игнорируй HTML теги (<b>, <i>, и т.п.) — бери только текст. Если не найдено — придумай по контексту ингредиентов
- ingredients: массив групп ингредиентов. Игнорируй HTML теги, рекламу, ссылки, призывы подписаться, комментарии
- steps: только реальные шаги приготовления. Игнорируй HTML теги. Ищи шаги даже если они идут списком без явного заголовка "Приготовление"
- time: только если явно указано ("30 мин", "1 час"), иначе null
- servings: только если явно указано числом, иначе null
- tags: 3-5 ключевых слов о блюде (не о канале и не о рекламе)

ПРАВИЛА ДЛЯ ИНГРЕДИЕНТОВ:
- Если ингредиенты разбиты на группы ("Для теста:", "Для крема:") — создай отдельную группу для каждой с title
- Если группы отсутствуют — один объект с title: null
- items: копируй ингредиенты дословно, без HTML тегов

ПРИЗНАКИ МУСОРА (игнорировать):
- HTML теги и их содержимое как разметка (<b>, <i>, </b>, </i> и т.п.)
- Ссылки (http, t.me, @username)
- Призывы ("подпишись", "переходи", "заработай", "узнай")
- Реклама и промо
- Комментарии других пользователей
- Названия каналов и групп

Формат ответа:
{
    "title": "название",
    "ingredients": [
        { "title": null, "items": ["ингредиент 1", "ингредиент 2"] },
        { "title": "Для крема", "items": ["ингредиент 1"] }
    ],
    "steps": ["шаг 1", "шаг 2"],
    "time": "30 мин или null",
    "servings": 2,
    "tags": ["тег1", "тег2"]
}

Текст: ${text}
`;